function sum(a, b) {
  return a + b;
}
function substract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}
function divide(a, b) {
  return a / b;
}
function power(a, b) {
  return a ** b;
}
function round(a) {
  return Math.round(a);
}
function roundUp(a) {
  return Math.ceil(a);
}
